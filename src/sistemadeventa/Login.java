    static void ConsultarDatos(String contra) throws SQLException {
        try {
            con.conexionReturn();

            String usuario = Login.txtusuario.getText();
            ResultSet respuesta = con.consultarRegistros("SELECT idusuario, usuario, password, idtipousuario FROM tb_usuario WHERE usuario='" + usuario + "' AND password='" + contra + "'");

            Object[] Usuario = new Object[4];
            modelo = (DefaultTableModel) tlogin.getModel();

            modelo.setRowCount(0);

            if (respuesta.next()) {
                Usuario[0] = respuesta.getString("idusuario");
                Usuario[1] = respuesta.getString("usuario");
                Usuario[2] = respuesta.getString("password");
                Usuario[3] = respuesta.getString("idtipousuario");

                modelo.addRow(Usuario);
                tlogin.setModel(modelo);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }


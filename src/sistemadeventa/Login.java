    private void btniniciarsesionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btniniciarsesionActionPerformed
        try {
            String contra = String.valueOf(txtpassword.getPassword());
            ConsultarDatos(contra);

            if (Login.txtusuario.getText().equals("") || Login.txtpassword.getText().equals("")
                    || Login.txtusuario.getText().equals("Ingrese su nombre de usuario") || Login.txtpassword.getText().equals("Ingrese su contraseña")) {
                JOptionPane.showMessageDialog(null, "Ingresa usuario y contraseña.");
                txtusuario.setText("");
                txtpassword.setText("");
            } else if (tlogin.getRowCount() > 0) {
                JOptionPane.showMessageDialog(null, "¡Bienvenido al sistema!");

                idusuario = tlogin.getValueAt(0, 0).toString();

                Menu menu = new Menu();
                menu.setVisible(true);
                this.dispose();

            } else {
                JOptionPane.showMessageDialog(null, "Usuario o contraseña incorrectos. Intenta de nuevo.");
                txtusuario.setText("");
                txtpassword.setText("");
            }

        } catch (SQLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btniniciarsesionActionPerformed

